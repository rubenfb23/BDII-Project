CREATE TABLE ALERON_TRAS(
    ID_ALERONTRAS NUMBER(6) NOT NULL,
    ID_COMPONENTE NUMBER(6),
    PRIMARY KEY(ID_ALERONTRAS),
    FOREIGN KEY(ID_COMPONENTE) REFERENCES COMPONENTES

